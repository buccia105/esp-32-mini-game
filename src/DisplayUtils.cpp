// DISPLAY FUNCTIONS
// display initialization
void initDisplay() {
  if (!display.begin(SSD1306_SWITCHCAPVCC, DISP_ADDR)) {
    Serial.println(F("SSD1306 allocation failed"));
    for (;;)
      ;
  }
  display.clearDisplay();
  display.display();
}

// turn off display
void turnOffDisplay() {
  display.clearDisplay();
  display.display();
  display.ssd1306_command(SSD1306_DISPLAYOFF);
}


// SCREENS
// shutdown message
/**
  * @brief draws a shutdown pop-up
  */
void drawShutdownScreen() {
  display.clearDisplay();
  display.setTextColor(SSD1306_WHITE);
  display.setTextSize(1);
  display.setTextWrap(false);

  display.setCursor(14, 25);
  display.print("going to sleep...");

  display.setCursor(11, 33);
  display.print("release to turn off");

  display.display();
}

// wakeup message
/**
  * @brief draws a wakeup message
  */
void drawWakeupScreen() {
  display.clearDisplay();
  display.setTextColor(SSD1306_WHITE);

  display.setTextSize(2);
  display.setTextWrap(false);
  display.setCursor(17, 10);
  display.print("WELCOME!");

  display.setTextSize(1);
  display.setCursor(35, 33);
  display.print("created by");
  display.setCursor(26, 45);
  display.print("Buccia & Fior");

  display.display();
}

// main manu
/**
  * @brief draws menu UI.
  *
  * @param selectedIndex is the index of the currently selected app. 
  */
void drawMenu(int selectedIndex) {
  display.clearDisplay();

  int boxSize = 35;
  int iconSize = 25;
  int spacing = 6;
  int startX = 5;
  int startY = 14;
  int iconOffset = 5; 

  for (int i = 0; i < 3; i++) {
    int xPos = startX + (i * (boxSize + spacing));

    if (i == selectedIndex) {
      // inverting icon colors for the selected item
      display.fillRect(xPos, startY, boxSize, boxSize, SSD1306_WHITE);
      display.drawBitmap(xPos + iconOffset, startY + iconOffset, menu_icons[i],
                         iconSize, iconSize, SSD1306_BLACK);
    } else {
      // unselected drawing white rectangle
      display.drawRect(xPos, startY, boxSize, boxSize, SSD1306_WHITE);
      display.drawBitmap(xPos + iconOffset, startY + iconOffset, menu_icons[i],
                         iconSize, iconSize, SSD1306_WHITE);
    }
  }

  display.display();
}

// weather app
/**
  * @brief draws appropriate icons for the weather status.
  *
  * @param x the starting X coordinate on the display (0-127).
  * @param y the starting Y coordinate on the display (0-63).
  * @param condition the text condition returned by the API. 
  */
void drawWeatherStatusIcon(int x, int y, String condition) {
  //mapping openweathermap API to a single or more icons
  if (condition == "Clear") {
    display.drawBitmap(x, y, image_weather_sun_bits, 16, 16, SSD1306_WHITE);
  } else if (condition == "Clouds") {
    // Usiamo l'icona con sole e nuvola per "Clouds"
    display.drawBitmap(x, y, image_weather_cloud_sunny_bits, 24, 16,
                       SSD1306_WHITE);
  } else if (condition == "Rain" || condition == "Drizzle" ||
             condition == "Thunderstorm") {
    display.drawBitmap(x, y, image_weather_cloud_rain_bits, 24, 16,
                       SSD1306_WHITE);
  } else if (condition == "Mist" || condition == "Fog" || condition == "Haze") {
    display.drawBitmap(x, y, image_cloud_bits, 24, 16, SSD1306_WHITE);
  } else {
    // Icona di default (nuvola generica) per stati non gestiti (es. Snow)
    display.drawBitmap(x, y, image_cloud_bits, 24, 16, SSD1306_WHITE);
  }
}